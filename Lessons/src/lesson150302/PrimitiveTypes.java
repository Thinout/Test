package lesson150302;

public class PrimitiveTypes {

	
	public static void main(String[] args) {
		
		byte b = 0;  // 8 bits == 1 bytes
		short s = 0;  // 16 bits == 2 bytes
		int i = 0;  // 32 bits == 4 bytes
		long l = 0; // 64 bits == 8 bytes
		
		char c = 'a';  //  16 bits = 2 bytes
		
		char c2 = c++;
		char c3 = 100;
		
		boolean bool = true; // 1 byte
		
		float f = 0.0f;  // 32 bits
		
		double d = 0.0; // 64 bits
		
		
		String str = "Hello";  // 32 bits
		
		Object o; // 32 bits   null
		
//		System.out.println(o);
	}
	
}
